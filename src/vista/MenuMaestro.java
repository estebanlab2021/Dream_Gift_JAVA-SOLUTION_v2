    public void llamarDatosCateArticulo(){
        try{
            int fila = TableCategoriaArt.getSelectedRow();
            int ID = (int) TableCategoriaArt.getValueAt(fila, 0);
            
            PreparedStatement ps = null;
            ResultSet rs = null;
            
            Conexion conn = new Conexion();
            Connection con = conn.getConexion();
            
            String sql = "SELECT * FROM categoria_articulo WHERE (idcategoria_articulo = ?)";
            
            ps = con.prepareStatement(sql);
            ps.setInt(1, ID);
            rs = ps.executeQuery();
            
            while(rs.next()){
                
                txtIdCategoria_Articulo.setText(String.valueOf(rs.getString("idcategoria_articulo")));
                txtCategoriaArticulo.setText(rs.getString("categoria_articulo"));
                if(rs.getString("estado").equals("1")){
                    RadioButtonEstado.setSelected(true);
                }else if(rs.getString("estado").equals("0")){
                    RadioButtonEstado0.setSelected(true);
                }
            }
            
        }catch(SQLException ex){
            System.err.println(ex.toString());
        }
    }
    
